PI = 3.141592
raio = float(input(""))
circunferencia = 2 * PI * raio
area_circulo = PI * raio**2
area_esfera = 4 * PI * raio**2
vol_esfera = 4 / 3 * PI * raio**3
print("{:.6f}".format(circunferencia))
print("{:.6f}".format(area_circulo))
print("{:.6f}".format(area_esfera))
print("{:.6f}".format(vol_esfera))

